#!/bin/bash

function runScript {
  local script=$1

  set -- ${@:2}
  echo 
  echo '############################################################################################################################################'
  echo "Executing . /build/${script}.sh $@"
  echo '############################################################################################################################################'
  . /build/${script}.sh $@

  if [[ $? != 0 ]]; then
    echo "Error executing: . /build/${script}.sh $@"
    exit 1
  fi
}
